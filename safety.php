        <h1>Precautions To Take While Using a CNG Car</h1>
        <p>Many people travel daily by cars, espacially in metro cities.
                 While some people travel due to work or to take their kids 
                 to school,  others <br>commute every day to reach their colleges
                  or institutes, As a result, a lot of people have started using 
                  CNG cars or have  installed a CNG <br>kit in their petrol  cars, 
                  CNG stands for compressed natural gas which is an odourless,
                   clear and non-corrosive  natural gas mostly <br>comprised of
                    methane gas, such as petrol.
                </p>
            <p>CNG is no doubt a safer fuel than petrol or diesel. Nonetheless,
                a car owner must exercise some precautions while using a CNG  in 
                his<br> car to ensure the safety of the car, its passengers and 
                the surroundings, Take a look at 14 precautions that every CNG car  owner
                 must<br> take while using a CNG car.</p>

                 <h2>1. &nbsp; Do Not Use Unauthorized CNG Kits</h2>

            <p>Although people are advised to buy a CNG car with a factory-fitted CNG kit,
                 many people try to convert their petrol cars into  CNG by <br>getting 
                 the fuel kit installed in their Vehicle. On top of that, they get 
                 the installation done by a roadside mechanic or  dealer instead of <br>
                 an authorized dealer to save money. This not only messes up the 
                 mechanics of the car but can also result in  leakage leading to a fire.<br>
                  Thus, a car owner must avoid installing unauthorized CNG kit in his car.</p>

                  <h2>2. &nbsp; Avoid Using Mismatched/Substandard Accessories</h2>
            <p>While installing a CNG kit in the car, a car owner must ensure that the fuel kit and 
                cylinder being installed matches with the make and <br>model of the car. The CNG kit and 
                cylinder must be compatible with the original wiring and fixtures of the car. Moreover,
                 the CNG <br>cylinder to be installed should be of an approved brand and manufactured 
                 as per revelent standards. If mismatched or substandard <br> CNG kit is fitted in the car,
                  the chances of a fire or damage increases due to difference in voltage and current ratings.</p>


                  <h2>3. &nbsp; Replace Spark Plugs</h2>
            <p>While converting a petrol car into CNG, the car owner should ensure that the spark 
                plug of the vehicle is compatible with CNG kit.<br> This is important as spark plugs 
                tend to wear out fast when used on CNG. If the special CNG-compatible spark plug is 
                not available,<br> the gap between the source of spark and the metallic tip of the plug
                 should reduced.</p>

                 <h2>4. &nbsp; Get CNG Cylinder Test Certificate</h2>
            <p>Once the CNG kit has been installed, the car owner must obtain a CNG 
                Cylinder Test Certificates by the CNG Retrofitter or licensee, <br>
                The certificates acts as a proff for installing approved CNG cylinder in the car.</p>


                <h2>5. &nbsp; First Petrol, Then CNG</h2>
            <p>A CNG car diver should always start his car on petrol and switch to CNG after running the 
                vehical for atleast 1KM. Doing so helps the <br>car engine to stay lubricated and reduces its wear and tear.</p>

                <h2>7. &nbsp; No Smoking inside the Car</h2>
            <p>The owner of a CNG car should not smoke inside his car. This is because smoking
                 can lead to a fire in the CNG car which can have <br>devasting consequences.

            </p>


            <h2>8. &nbsp; Follow CNG Car Usage Rules</h2>
            <p>CNG car owners must adhere to all vehicle usage rules as per the motor vehicle laws. 
                They should not use a car heater for a prolonged <br>period, transport flammable materials
                 or burst crackers near the car. Defying these rules can be extremely dangerous as CNG 
                 car can<br> easily reach its ignition temperature of 540 degrees Celsius while being
                  used and can catch fire. Since the car damage would be a result<br> of the car owner’s 
                  carelessness, even the best car insurance companies may reject the claims.</p>
                  <h2>9. &nbsp; Turn Off the Car Before Refilling CNG</h2>
            <p>Before the gas station technician begins to refill CNG in the car,
                 the driver must ensure that the vehical engine is turned off and the key <br>
                 is turned to the ‘off’ position. This ensure that there are no sparks in the 
                 car’s engine and thus, no fire.</p>


                 <h2>10. &nbsp; Don’t Use Mobile Phones/Electronics While Refilling </h2>
            <p>While getting CNG refilled in the car, the vehicle owner must ensure 
                that he or any of his co-passenger are not using their mobile phones <br>
                or any electronics as it can lead to a fire.</p>


                <h2>11. &nbsp; Keep an Eye on the Cylinder Pressure</h2>
            <p>While refilling a CNG cylinder at a gas station, the car owner must 
                ensure that the CNG cylinder pressure is more than 200 bars. Doing <br>
                so avoids overfilling of the CNG cylinder and subsequent leakage.
                 The cylinder pressure can be checked by monitoring the gauge fitted <br>
                 at the gas filling machine.</p>



                 <h2>12. &nbsp; Refill CNG Up to Two-Third of Cylinder Capacity</h2>
            <p>During the summer months, a CNG cylinder should not be refilled more 
                than two-thirds of its total capacity. This is because the<br>
                 temperatures are soaring high in summers and thus, a full fuel tank can be dangerous.</p>

                 <h2>13. &nbsp; Try to Park Vehicles in Shade or Covered Area </h2>
            <p>A CNG car owner should always try to park his vehicle in a covered
                 or shaded area, especially in the daytime. This is important to avoid<br>
                  direct exposure of the car in the sun which can easily heat the car making it prone to fire.</p>


                  <h2>In a Nutshell</h2>
            <p>
                Driving responsibly and taking the necessary precautions is sure to let a
                 person have a seamless CNG car experience. Every CNG car owner <br> must  take 
                 the necessary precautions to ensure the safety of his car and its passengers. 
                 Moreover, he must make car insurance comparison to<br> choose the best car insurance 
                 with preferably a comprehensive cover so that any loss or damages to the car is taken care of.
                </p>
                 
